$('.modal-button').on('click', function () {
	var $this =  $(this);
	var $parent = $this.closest('.modal-wrapper');
	var $child = $parent.find('.modal');
	setTimeout(function () {
		$child.fadeIn();
		$('.close').css({
			'width': 60,
			'height': 60
		});
		$('body').addClass('modal-open');
	}, 200);
	return false;
});
